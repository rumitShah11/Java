public class escapeSequences {
    public static void main(String[] args) {

        System.out.println("Right Half Pyramid Pattern");
        System.out.println(" *\n *\t*\n *\t*\t*\n *\t*\t*\t*\n *\t*\t*\t*\t*\n");

        // Right Half Pyramid Pattern
        // *
        // *	*
        // *	*	*
        // *	*	*	*
        // *	*	*	*	*

        System.out.println("Reverse Right Half Pyramid Pattern");
        System.out.println(" *\t*\t*\t*\t*\n *\t*\t*\t*\n *\t*\t*\n *\t*\n *\n");

//        Reverse Right Half Pyramid Pattern
//                *	*	*	*	*
//                *	*	*	*
//                *	*	*
//                *	*
//                *

        System.out.println("Left Half Pyramid Pattern");
        System.out.println("\t\t\t\t*\n \t\t\t*\t*\n \t\t*\t*\t*\n \t*\t*\t*\t*\n *\t*\t*\t*\t*\n");

//        Left Half Pyramid Pattern
//				    *
// 			    *	*
// 		    *	*	*
// 	    *	*	*	*
//  *	*	*	*	*
    }
}
