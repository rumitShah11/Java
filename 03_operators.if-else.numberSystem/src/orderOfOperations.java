public class orderOfOperations {
    public static void main(String[] args) {


        /*
            B               O               D       M               A       S
         (Bracket)       (Order)          ( /  or   * )           ( +  or   - )
        */

        System.out.println(8-3*3);
        System.out.println(9/3*2/6);
        System.out.println(8-5+7-1);
        System.out.println(9/(3/3 + 2));
    }
}